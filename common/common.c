/*******************************************************************************
*                                                                              *
* PROCEDURE:                                                                   * 
* 		disable_irq                                                    *
*                                                                              *
* DESCRIPTION:                                                                 * 
* 		Disables IRQ interrupts; wrapper for __disable_irq()           *
* 		                                                               *
*                                                                              *
*******************************************************************************/
void disable_irq
    (
    void
    ) 
{
__disable_irq();
} /* disable_irq */


/*******************************************************************************
*                                                                              *
* PROCEDURE:                                                                   * 
* 		enable_irq                                                     *
*                                                                              *
* DESCRIPTION:                                                                 * 
* 		Enables IRQ interrupts; wrapper for __enable_irq()             *
* 		                                                               *
*                                                                              *
*******************************************************************************/
void enable_irq
    (
    void
    ) 
{
__enable_irq();
} /* enable_irq */


/*******************************************************************************
*                                                                              *
* PROCEDURE:                                                                   * 
* 		delay_ms                                                               *
*                                                                              *
* DESCRIPTION:                                                                 * 
* 		Minimum delay in miliseconds                                           *
*                                                                              *
*******************************************************************************/
void delay_ms
    (
    uint32_t delay
    )
{
HAL_Delay(delay);

} /* delay_ms */


